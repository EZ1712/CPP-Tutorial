#include <iostream>

using namespace std;

int main() {

    int a = 5;
    int b = 5;

    // increment dan decrement
    // preincrement dan postencrement

    // preincrement
    cout << a << endl;
    cout << a++ << endl;
    cout << a << endl << endl;
    
    // postincrement
    cout << b << endl;
    cout << ++b << endl;
    cout << b << endl;

    return 0;
}